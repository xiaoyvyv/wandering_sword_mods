local function register_all_class_functions()
    if enableTraceFunction then
        local hookCount = 0
        local skipCount = 0

        -- 将排除列表转换为键值对映射，实现极速查找
        local exclude_map = {}
        if exclude then
            for _, path in ipairs(exclude) do
                exclude_map[path] = true
            end
        end

        for _, funcName in ipairs(hook_functions) do
            if funcName and funcName ~= "" then
                -- 核心判断：如果命中排除列表，直接跳过并计数
                if exclude_map[funcName] then
                    skipCount = skipCount + 1
                else
                    local status, err = pcall(function()
                        RegisterHook(funcName, function()
                            log("[Trace Hit] 触发了内部调用: " .. funcName)
                        end)
                    end)

                    if status then
                        hookCount = hookCount + 1
                    else
                        log("[Warning] 无法 Hook 方法: " .. funcName .. " | 原因: " .. tostring(err))
                    end
                end
            end
        end

        log(string.format("成功注册并监听了 %d 个子方法，主动过滤了 %d 个高频方法", hookCount, skipCount))
    end
end

local function init_mod()
    mNPCFuncLib = StaticFindObject("/Script/JH.Default__NPCFuncLib")

    if not mNPCFuncLib or not mNPCFuncLib:IsValid() then
        ExecuteWithDelay(RETRY_INTERVAL_MS * 2, init_mod)
        return
    end

    register_all_class_functions()
end

local function register_mod(init_callback)
    local ok = pcall(function()
        return UEHelpers.GetPlayerController()
    end)

    if ok then
        init_callback()
        return
    end

    local pre_id
    local post_id
    pre_id, post_id = RegisterHook("/Script/Engine.PlayerController:ClientRestart", function()
        if pre_id and post_id then
            UnregisterHook("/Script/Engine.PlayerController:ClientRestart", pre_id, post_id)
        end
        ExecuteWithDelay(RETRY_INTERVAL_MS, init_callback)
    end)
end

register_mod(init_mod)
log("模组加载成功")
